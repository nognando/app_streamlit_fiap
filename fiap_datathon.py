"""#MODELO DE MACHINE LEARNING"""

import pandas as pd
import numpy as np
import joblib

from sklearn.model_selection import GroupShuffleSplit, cross_validate
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.impute import SimpleImputer
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import (
    accuracy_score,
    balanced_accuracy_score,
    f1_score,
    roc_auc_score,
    precision_score,
    recall_score,
    classification_report,
    confusion_matrix
)

df = pd.read_excel("df_modelo.xlsx").copy()

df.columns = [c.strip().lower() for c in df.columns]

df = df.sort_values(["ra", "ano"]).reset_index(drop=True)

df

# Criando o target

df["defasagem_next"] = df.groupby("ra")["defasagem"].shift(-1)
df["ano_next"] = df.groupby("ra")["ano"].shift(-1)

# manter apenas transições válidas de um ano para o outro
df_ml = df[df["ano_next"] == df["ano"] + 1].copy()

# target binário
df_ml["target_risco_defasagem"] = (df_ml["defasagem_next"] < 0).astype(int)

print("Shape base ML:", df_ml.shape)
print("\nDistribuição do target:")
print(df_ml["target_risco_defasagem"].value_counts(dropna=False))
print(df_ml["target_risco_defasagem"].value_counts(normalize=True).round(4))

# FEATURES

# IMPORTANTE:
# não usar "defasagem_next" nem qualquer coluna futura
# e evitar usar "defasagem" atual como feature principal,
# porque isso pode deixar o modelo excessivamente dependente
# do próprio conceito que ele tenta prever.
#
# Também optei por NÃO usar IAN aqui.
# Motivo: IAN é muito próximo da ideia de adequação ao nível,
# então pode funcionar como proxy forte demais da própria defasagem.
# Se quiser, depois testamos uma versão com IAN para comparar.

features_num = [
    "idade",
    "ano_ingresso",
    "iaa",
    "ida",
    "ian",
    "ieg",
    "ips",
    "ipp",
    "ipv",
    "inde",
    "matematica",
    "portugues",
    "ingles",
    "qtde_av",
]

features_cat = [
    "ano",
    "fase",
    "fase_ideal",
    "pedra",
    "genero",
    "instituicao_ensino",
    "turma",
]

features = features_num + features_cat

# manter apenas colunas existentes
features = [c for c in features if c in df_ml.columns]
features_num = [c for c in features_num if c in df_ml.columns]
features_cat = [c for c in features_cat if c in df_ml.columns]

# converter categóricas para string
for c in features_cat:
    df_ml[c] = df_ml[c].astype(str)

X = df_ml[features].copy()
y = df_ml["target_risco_defasagem"].copy()
groups = df_ml["ra"].copy()

# PRÉ-PROCESSAMENTO
preprocessor = ColumnTransformer(
    transformers=[
        (
            "num",
            Pipeline(
                steps=[
                    ("imputer", SimpleImputer(strategy="median")),
                    ("scaler", StandardScaler()),
                ]
            ),
            features_num,
        ),
        (
            "cat",
            Pipeline(
                steps=[
                    ("imputer", SimpleImputer(strategy="most_frequent")),
                    ("onehot", OneHotEncoder(handle_unknown="ignore")),
                ]
            ),
            features_cat,
        ),
    ]
)

# MODELO
# LogisticRegression

model = LogisticRegression(
    max_iter=2000,
    class_weight="balanced",
    random_state=42
)

pipeline = Pipeline(
    steps=[
        ("preprocessor", preprocessor),
        ("model", model),
    ]
)


# VALIDAÇÃO ROBUSTA

# usando GroupShuffleSplit por RA para evitar vazamento entre
# treino e teste do mesmo aluno
gss = GroupShuffleSplit(n_splits=5, test_size=0.25, random_state=42)

cv_scores = cross_validate(
    pipeline,
    X,
    y,
    groups=groups,
    cv=gss,
    scoring=[
        "accuracy",
        "balanced_accuracy",
        "f1",
        "roc_auc",
        "precision",
        "recall",
    ],
    return_train_score=False,
)

print("\nMÉTRICAS MÉDIAS - CROSS VALIDATION")
for metric_name, values in cv_scores.items():
    if metric_name.startswith("test_"):
        print(f"{metric_name}: {values.mean():.4f} ± {values.std():.4f}")

# TREINO FINAL

# separação final em treino/teste também por RA
splitter = GroupShuffleSplit(n_splits=1, test_size=0.25, random_state=42)
train_idx, test_idx = next(splitter.split(X, y, groups=groups))

X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]

pipeline.fit(X_train, y_train)

y_pred = pipeline.predict(X_test)
y_proba = pipeline.predict_proba(X_test)[:, 1]

print("\nMÉTRICAS HOLDOUT")
print(f"Accuracy:          {accuracy_score(y_test, y_pred):.4f}")
print(f"Balanced Accuracy: {balanced_accuracy_score(y_test, y_pred):.4f}")
print(f"F1-score:          {f1_score(y_test, y_pred):.4f}")
print(f"Precision:         {precision_score(y_test, y_pred):.4f}")
print(f"Recall:            {recall_score(y_test, y_pred):.4f}")
print(f"ROC AUC:           {roc_auc_score(y_test, y_proba):.4f}")

print("\nClassification Report")
print(classification_report(y_test, y_pred))

print("\nMatriz de Confusão")
print(confusion_matrix(y_test, y_pred))


# EXPORTAÇÃO
# salva pipeline completa: preprocessamento + modelo
joblib.dump(pipeline, "modelo_risco_defasagem.joblib")

# opcional: salvar nomes das features esperadas pelo streamlit
joblib.dump(features, "modelo_risco_defasagem_features.joblib")

