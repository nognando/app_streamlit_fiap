import pandas as pd
import re

def _clean_col(c: str) -> str:
    c = str(c).strip()
    c = c.replace("\n", " ")
    c = re.sub(r"\s+", " ", c)
    return c

def _dedup_columns(cols):
    seen = {}
    out = []
    for c in cols:
        if c not in seen:
            seen[c] = 1
            out.append(c)
        else:
            seen[c] += 1
            out.append(f"{c}__{seen[c]}")
    return out

def padroniza_df(df: pd.DataFrame, ano: int, manter_historico_inde=False, manter_historico_pedra=False) -> pd.DataFrame:
    df = df.copy()

    df.columns = [_clean_col(c) for c in df.columns]
    df.columns = _dedup_columns(df.columns)

    ren = {
        "RA": "ra",
        "Fase": "fase",
        "Turma": "turma",
        "Nome": "nome",
        "Nome Anonimizado": "nome",
        "Ano nasc": "data_nasc",
        # "Data de Nasc": "data_nasc",
        "Idade 22": "idade",
        "Idade": "idade",
        "Gênero": "genero",
        "Ano ingresso": "ano_ingresso",
        "Instituição de ensino": "instituicao_ensino",
        "Nº Av": "qtde_av",
        "Rec Psicologia": "rec_psicologia",
        "Indicado": "indicado_bolsa",
        "Atingiu PV": "ponto_virada",
        "Matem": "matematica",
        "Mat": "matematica",
        "Portug": "portugues",
        "Por": "portugues",
        "Inglês": "ingles",
        "Ing": "ingles",
        "Fase ideal": "fase_ideal",
        "Fase Ideal": "fase_ideal",
        "Defas": "defasagem",
        "Defasagem": "defasagem",
        "Destaque IEG": "destaque_ieg",
        "Destaque IDA": "destaque_ida",
        "Destaque IPV": "destaque_ipv",
    }
    df = df.rename(columns=ren)

    inde_candidates = [
        f"INDE {ano}",
        f"INDE {str(ano)[-2:]}",
        f"INDE {str(ano)[-2:]}__2",
        f"INDE {ano}__2",
    ]
    inde_col = next((c for c in inde_candidates if c in df.columns), None)
    if inde_col:
        df = df.rename(columns={inde_col: "inde"})

    pedra_candidates = [
        f"Pedra {ano}",
        f"Pedra {str(ano)[-2:]}",
        f"Pedra {str(ano)[-2:]}__2",
        f"Pedra {ano}__2",
    ]
    pedra_col = next((c for c in pedra_candidates if c in df.columns), None)
    if pedra_col:
        df = df.rename(columns={pedra_col: "pedra"})

    for i in range(1, 7):
        c = f"Avaliador{i}"
        if c in df.columns:
            df = df.rename(columns={c: f"avaliador{i}"})

    for i in range(1, 5):
        c = f"Rec Av{i}"
        if c in df.columns:
            df = df.rename(columns={c: f"rec_av{i}"})

    metrics = ["IAA", "IEG", "IPS", "IPP", "IDA", "IPV", "IAN"]
    df = df.rename(columns={m: m.lower() for m in metrics if m in df.columns})

    if not manter_historico_inde:
        drop_inde = [c for c in df.columns if re.fullmatch(r"INDE\s(\d{2}|\d{4})(__\d+)?", c) and c != "inde"]
        df = df.drop(columns=drop_inde, errors="ignore")

    if not manter_historico_pedra:
        drop_pedra = [c for c in df.columns if re.fullmatch(r"Pedra\s\d{2,4}(__\d+)?", c) and c != "pedra"]
        df = df.drop(columns=drop_pedra, errors="ignore")

    df["ano"] = ano
    df.columns = [c.lower() for c in df.columns]

    # normalização textual
    for col in df.select_dtypes(include="object").columns:
        df[col] = df[col].astype(str).str.strip()

    # conversão de colunas numéricas importantes
    colunas_numericas = [
        "idade", "ano_ingresso", "inde", "iaa", "ieg", "ips", "ipp", "ida", "ipv", "ian",
        "matematica", "portugues", "ingles", "qtde_av"
    ]
    for col in colunas_numericas:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    return df

